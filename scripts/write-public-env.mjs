#!/usr/bin/env node
/**
 * Reads process.env and writes src/environments/environment.prod.ts
 * (and optionally patches local environment.ts when WRITE_LOCAL=1).
 *
 * Usage (Vercel / CI prebuild):
 *   API_BASE_URL=https://….ondigitalocean.app \
 *   TURNSTILE_SITE_KEY=0x… \
 *   APP_ENV=production \
 *   node scripts/write-public-env.mjs
 */
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

function read(name, fallback) {
  const value =
    process.env[name] ??
    process.env[`NG_APP_${name}`] ??
    process.env[`NEXT_PUBLIC_${name}`];
  return value && String(value).trim() ? String(value).trim() : fallback;
}

const defaults = {
  API_BASE_URL: 'https://api.kolss.example',
  SITE_CODE: 'kolss-ua',
  PRIVACY_POLICY_VERSION: 'ua-v1',
  TURNSTILE_SITE_KEY: '',
  APP_ENV: 'production',
};

const env = {
  API_BASE_URL: read('API_BASE_URL', defaults.API_BASE_URL),
  SITE_CODE: read('SITE_CODE', defaults.SITE_CODE),
  PRIVACY_POLICY_VERSION: read('PRIVACY_POLICY_VERSION', defaults.PRIVACY_POLICY_VERSION),
  TURNSTILE_SITE_KEY: read('TURNSTILE_SITE_KEY', defaults.TURNSTILE_SITE_KEY),
  APP_ENV: read('APP_ENV', defaults.APP_ENV),
};

const body = `/**
 * AUTO-GENERATED by scripts/write-public-env.mjs — do not edit by hand.
 * Generated at ${new Date().toISOString()}
 */
export const environment = ${JSON.stringify(env, null, 2)} as const;
`;

const target =
  process.env.WRITE_LOCAL === '1'
    ? resolve(root, 'src/environments/environment.ts')
    : resolve(root, 'src/environments/environment.prod.ts');

writeFileSync(target, body, 'utf8');
console.log(`[write-public-env] wrote ${target}`);
console.log(`[write-public-env] API_BASE_URL=${env.API_BASE_URL} APP_ENV=${env.APP_ENV}`);
